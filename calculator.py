class calculator:
    
    def addition(x,y):
        add=x+y
        print(add)
    def substraction(x,y):
        subs=x-y
        print(subs)  
    def multiplication(x,y):
        mul=x*y 
        print(mul)
    def division(x,y):
        div=x/y
        print(div)
    def modulus(x,y):
        mod=x%y
        print(mod)
    def exponential(x,y):
        pow=x**y;
        print(pow)
        
        
